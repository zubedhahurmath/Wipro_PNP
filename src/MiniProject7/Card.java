package MiniProject7;

public class Card {
	 private char symbol;
	    private int number;

	    public Card(char symbol, int number) {
	        this.symbol = symbol;
	        this.number = number;
	    }

	    public char getSymbol() {
	        return symbol;
	    }

	    public int getNumber() {
	        return number;
	    }

}
